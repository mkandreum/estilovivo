import React, { useState, useEffect } from 'react';
import { X, Share2, Heart, CreditCard, MessageCircle, Truck, Store, Copy, Send, Eye } from 'lucide-react';

export interface ProductDisplayItem {
  id: string | number;
  title: string;
  price: number;
  image: string;
  user: string;
  avatar?: string;
  description?: string;
  brand?: string;
  size?: string;
  condition?: string;
  isOwnItem?: boolean;
}

interface ProductDetailModalProps {
  product: ProductDisplayItem;
  onClose: () => void;
  onEdit?: () => void;
  onDelete?: () => void;
  onAddToTrip?: () => void;
  onMessage?: (product: ProductDisplayItem) => void;
  onShareFeed?: (product: ProductDisplayItem) => void;
}

const ProductDetailModal: React.FC<ProductDetailModalProps> = ({ product, onClose, onEdit, onDelete, onAddToTrip, onMessage, onShareFeed }) => {
  const [showBuyOptions, setShowBuyOptions] = useState(false);
  const [showShareOptions, setShowShareOptions] = useState(false);
  const [isLiked, setIsLiked] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const [copied, setCopied] = useState(false);

  // Load initial favorite status
  useEffect(() => {
    const loadFavoriteStatus = async () => {
      try {
        const token = localStorage.getItem('token');
        if (!token || !product.id) return;
        
        const res = await fetch('/api/social/favorites', {
          headers: { 'Authorization': `Bearer ${token}` }
        });
        const favorites = await res.json();
        const isFavorited = favorites.some((fav: any) => fav.productId === product.id);
        setIsLiked(isFavorited);
      } catch (e) {
        console.warn('Failed to load favorite status:', e);
      }
    };
    loadFavoriteStatus();
  }, [product.id]);

  const galleryImages = [product.image];

  const handleShare = () => {
    setShowShareOptions(true);
  };

  const handleCopyLink = () => {
    navigator.clipboard.writeText(window.location.href);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleLike = async () => {
    setIsLoading(true);
    try {
      const token = localStorage.getItem('token');
      const res = await fetch('/api/social/favorite', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify({ productId: product.id })
      });
      const data = await res.json();
      setIsLiked(data.favorited);
    } catch (error) {
      console.error('Error toggling favorite:', error);
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="fixed inset-0 z-[60] flex items-end sm:items-center justify-center pointer-events-none p-4">
      {/* Backdrop */}
      <div 
        className="absolute inset-0 bg-black/60 backdrop-blur-sm pointer-events-auto transition-opacity" 
        onClick={onClose}
      />

      {/* Main Modal Card */}
      <div className="bg-white w-full max-w-md max-h-[95vh] sm:max-h-[90vh] rounded-t-3xl sm:rounded-3xl shadow-2xl z-50 flex flex-col overflow-hidden pointer-events-auto animate-fade-in-up transform transition-transform">
        
        {/* Header with Close and Actions */}
        <div className="sticky top-0 z-20 bg-white border-b border-gray-100 p-4 flex justify-between items-center">
          <button 
            onClick={onClose} 
            className="p-2 rounded-full hover:bg-gray-100 transition-colors"
          >
            <X size={24} className="text-gray-600" />
          </button>
          
          <h3 className="text-sm font-bold text-gray-800">Detalles</h3>
          
          <div className="flex gap-2">
            <button 
              onClick={handleShare}
              className="p-2 rounded-full hover:bg-gray-100 transition-colors text-gray-600"
            >
              <Share2 size={20} />
            </button>
            <button 
              onClick={handleLike} 
              disabled={isLoading}
              className="p-2 rounded-full hover:bg-gray-100 transition-colors disabled:opacity-50"
            >
              <Heart size={20} fill={isLiked ? "currentColor" : "none"} className={isLiked ? "text-rose-500" : "text-gray-600"} />
            </button>
          </div>
        </div>

        {/* Scrollable Content */}
        <div className="flex-1 overflow-y-auto">
          
          {/* Product Image Gallery */}
          <div className="w-full aspect-square bg-gray-100 relative overflow-hidden">
            <img src={product.image} className="w-full h-full object-cover" alt={product.title} />
            
            {/* Price Badge */}
            <div className="absolute top-4 right-4 bg-white rounded-xl px-3 py-2 shadow-md">
              <p className="text-xl font-bold text-primary">{product.price}€</p>
            </div>

            {/* Condition Badge */}
            {product.condition && (
              <div className="absolute bottom-4 left-4 bg-white/95 backdrop-blur-sm rounded-lg px-3 py-1.5">
                <p className="text-xs font-bold text-gray-700 capitalize">Estado: {product.condition}</p>
              </div>
            )}
          </div>

          {/* Product Details */}
          <div className="p-6 space-y-4">
            
            {/* Title and Brand */}
            <div>
              <h2 className="text-2xl font-bold text-gray-900 capitalize mb-1">{product.title}</h2>
              <p className="text-gray-600 font-medium">
                {product.brand || 'Marca desconocida'} {product.size ? `• Talla ${product.size}` : ''}
              </p>
            </div>

            {/* Seller Info Card */}
            <div className="bg-gradient-to-br from-primary/5 to-primary/10 rounded-2xl p-4 border border-primary/20">
              <div className="flex items-center justify-between mb-3">
                <div className="flex items-center gap-3 flex-1 min-w-0">
                  <img 
                    src={product.avatar || "https://ui-avatars.com/api/?name=User&background=random"} 
                    className="w-12 h-12 rounded-full object-cover border-2 border-white shadow-sm flex-shrink-0" 
                    alt={product.user}
                  />
                  <div className="min-w-0">
                    <p className="font-bold text-gray-800 truncate">{product.user}</p>
                    <div className="flex text-yellow-400 text-xs">
                      {'★'.repeat(5)} <span className="text-gray-400 ml-1">(24 reseñas)</span>
                    </div>
                  </div>
                </div>
                <button
                  onClick={() => onMessage?.(product)}
                  className="p-2.5 rounded-full bg-white hover:bg-gray-50 transition-colors flex-shrink-0"
                >
                  <MessageCircle size={18} className="text-primary" />
                </button>
              </div>
              <p className="text-xs text-gray-600">Vendedor verificado • Entrega rápida</p>
            </div>

            {/* Description */}
            <div className="space-y-2">
              <h3 className="font-bold text-gray-900">Descripción</h3>
              <p className="text-gray-700 text-sm leading-relaxed">
                {product.description || "Esta prenda está en excelentes condiciones. Usada muy pocas veces y cuidada con mucho amor. Perfecta para combinar en cualquier ocasión. Se entrega lavada y planchada."}
              </p>
            </div>

            {/* Details Grid */}
            <div className="grid grid-cols-2 gap-3">
              {product.brand && (
                <div className="bg-gray-50 rounded-xl p-3">
                  <p className="text-xs text-gray-600 font-semibold mb-1">Marca</p>
                  <p className="text-sm font-bold text-gray-800">{product.brand}</p>
                </div>
              )}
              {product.size && (
                <div className="bg-gray-50 rounded-xl p-3">
                  <p className="text-xs text-gray-600 font-semibold mb-1">Talla</p>
                  <p className="text-sm font-bold text-gray-800">{product.size}</p>
                </div>
              )}
              {product.condition && (
                <div className="bg-gray-50 rounded-xl p-3">
                  <p className="text-xs text-gray-600 font-semibold mb-1">Estado</p>
                  <p className="text-sm font-bold text-gray-800 capitalize">{product.condition}</p>
                </div>
              )}
              <div className="bg-gray-50 rounded-xl p-3">
                <p className="text-xs text-gray-600 font-semibold mb-1">Envío</p>
                <p className="text-sm font-bold text-gray-800">3,95€</p>
              </div>
            </div>

            {/* Spacing for bottom action buttons */}
            <div className="h-6" />
          </div>
        </div>

        {/* Bottom Actions - Sticky */}
        <div className="sticky bottom-0 bg-white border-t border-gray-200 p-4 space-y-3 shadow-[0_-4px_20px_rgba(0,0,0,0.08)]">
          {product.isOwnItem ? (
            /* Own Item Actions */
            <>
              <button 
                onClick={() => {
                  onAddToTrip?.();
                  onClose();
                }}
                className="w-full bg-gradient-to-r from-primary to-primary-dark text-white font-bold py-3 rounded-2xl shadow-lg shadow-primary/30 hover:shadow-xl transition-all active:scale-[0.98]"
              >
                ✈️ Añadir a Maleta
              </button>
              <div className="grid grid-cols-2 gap-3">
                <button 
                  onClick={() => {
                    onEdit?.();
                    onClose();
                  }}
                  className="bg-blue-50 text-blue-600 font-bold py-2.5 rounded-xl hover:bg-blue-100 transition-colors active:scale-[0.98]"
                >
                  Editar
                </button>
                <button 
                  onClick={() => {
                    onDelete?.();
                    onClose();
                  }}
                  className="bg-red-50 text-red-600 font-bold py-2.5 rounded-xl hover:bg-red-100 transition-colors active:scale-[0.98]"
                >
                  Eliminar
                </button>
              </div>
            </>
          ) : (
            /* Shop Item Actions */
            <>
              <button 
                onClick={() => setShowBuyOptions(true)}
                className="w-full bg-gradient-to-r from-primary to-primary-dark text-white font-bold py-3 rounded-2xl shadow-lg shadow-primary/30 hover:shadow-xl transition-all active:scale-[0.98] flex items-center justify-center gap-2"
              >
                <CreditCard size={20} />
                <span>Comprar Ahora</span>
              </button>
              <button 
                onClick={() => onAddToTrip?.()}
                className="w-full bg-blue-50 text-blue-600 font-bold py-2.5 rounded-xl hover:bg-blue-100 transition-colors active:scale-[0.98]"
              >
                ✈️ Añadir a Maleta
              </button>
            </>
          )}
        </div>

        {/* --- Share Options Modal --- */}
        {showShareOptions && (
          <div className="absolute inset-0 z-[70] bg-black/50 backdrop-blur-sm rounded-t-3xl sm:rounded-3xl flex flex-col items-end sm:items-center justify-end sm:justify-center p-4">
            <div className="bg-white w-full sm:max-w-sm rounded-t-3xl sm:rounded-3xl p-6 animate-fade-in-up shadow-2xl space-y-3">
              <div className="flex items-center justify-between mb-4">
                <h3 className="text-lg font-bold text-gray-900">Compartir</h3>
                <button 
                  onClick={() => setShowShareOptions(false)}
                  className="p-1 hover:bg-gray-100 rounded-full transition-colors"
                >
                  <X size={20} className="text-gray-600" />
                </button>
              </div>

              {/* Share Options */}
              <button 
                onClick={() => {
                  onShareFeed?.(product);
                  setShowShareOptions(false);
                  onClose();
                }}
                className="w-full flex items-center gap-4 p-4 border border-gray-200 rounded-2xl hover:border-primary hover:bg-primary/5 transition-all group"
              >
                <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-pink-400 to-rose-500 flex items-center justify-center shadow-md group-hover:scale-110 transition-transform flex-shrink-0">
                  <Eye size={24} className="text-white" />
                </div>
                <div className="text-left flex-1 min-w-0">
                  <p className="font-bold text-gray-800">Publicar en Social</p>
                  <p className="text-xs text-gray-600">Comparte con tu comunidad</p>
                </div>
              </button>

              <button 
                onClick={handleCopyLink}
                className="w-full flex items-center gap-4 p-4 border border-gray-200 rounded-2xl hover:border-primary hover:bg-primary/5 transition-all group"
              >
                <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-blue-400 to-cyan-500 flex items-center justify-center shadow-md group-hover:scale-110 transition-transform flex-shrink-0">
                  <Copy size={24} className="text-white" />
                </div>
                <div className="text-left flex-1 min-w-0">
                  <p className="font-bold text-gray-800">{copied ? 'Enlace copiado' : 'Copiar enlace'}</p>
                  <p className="text-xs text-gray-600">Comparte en otros sitios</p>
                </div>
              </button>

              {navigator.share && (
                <button 
                  onClick={() => {
                    navigator.share({
                      title: product.title,
                      text: `Mira este ${product.title} en Estilovivo`,
                      url: window.location.href,
                    }).catch(console.error);
                    setShowShareOptions(false);
                  }}
                  className="w-full flex items-center gap-4 p-4 border border-gray-200 rounded-2xl hover:border-primary hover:bg-primary/5 transition-all group"
                >
                  <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-purple-400 to-pink-500 flex items-center justify-center shadow-md group-hover:scale-110 transition-transform flex-shrink-0">
                    <Send size={24} className="text-white" />
                  </div>
                  <div className="text-left flex-1 min-w-0">
                    <p className="font-bold text-gray-800">Compartir</p>
                    <p className="text-xs text-gray-600">Mensajería y apps</p>
                  </div>
                </button>
              )}
            </div>
          </div>
        )}

        {/* --- BUY OPTIONS MODAL --- */}
        {showBuyOptions && (
          <div className="absolute inset-0 z-[70] bg-black/50 backdrop-blur-sm rounded-t-3xl sm:rounded-3xl flex flex-col items-end sm:items-center justify-end sm:justify-center p-4">
            <div className="bg-white w-full sm:max-w-sm rounded-t-3xl sm:rounded-3xl p-6 animate-fade-in-up shadow-2xl">
              <div className="flex items-center justify-between mb-4">
                <h3 className="text-lg font-bold text-gray-900">Elige cómo comprar</h3>
                <button 
                  onClick={() => setShowBuyOptions(false)}
                  className="p-1 hover:bg-gray-100 rounded-full transition-colors"
                >
                  <X size={20} className="text-gray-600" />
                </button>
              </div>

              <div className="space-y-3 mb-4">
                <button className="w-full flex items-center p-4 border border-gray-200 rounded-2xl hover:border-primary hover:bg-primary/5 transition-all group">
                  <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-emerald-400 to-green-500 flex items-center justify-center mr-4 group-hover:scale-110 transition-transform flex-shrink-0">
                    <Store size={20} className="text-white" />
                  </div>
                  <div className="text-left flex-1">
                    <p className="font-bold text-gray-800">En persona</p>
                    <p className="text-xs text-gray-600">Queda con {product.user.split(' ')[0]}</p>
                  </div>
                  <span className="text-sm font-bold text-emerald-600">Gratis</span>
                </button>

                <button className="w-full flex items-center p-4 border border-gray-200 rounded-2xl hover:border-primary hover:bg-primary/5 transition-all group">
                  <div className="w-12 h-12 rounded-xl bg-gradient-to-br from-orange-400 to-amber-500 flex items-center justify-center mr-4 group-hover:scale-110 transition-transform flex-shrink-0">
                    <Truck size={20} className="text-white" />
                  </div>
                  <div className="text-left flex-1">
                    <p className="font-bold text-gray-800">Envío a domicilio</p>
                    <p className="text-xs text-gray-600">Recíbelo en 24-48h</p>
                  </div>
                  <span className="text-sm font-bold text-gray-800">3,95€</span>
                </button>
              </div>

              <button className="w-full bg-gradient-to-r from-primary to-primary-dark text-white font-bold py-3 rounded-2xl shadow-lg hover:shadow-xl transition-all active:scale-[0.98]">
                Continuar
              </button>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

export default ProductDetailModal;